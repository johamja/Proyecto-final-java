    private void Boton_iniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Boton_iniciarActionPerformed
        // TODO add your handling code here:
        try {
            Primera_LINEA.P_Estado_primera_L = 1;
            Primera_LINEA.P_Estado_segunda_l = 1;
            Primera_LINEA.P_Estado_tercera_l = 1;

            JOptionPane.showMessageDialog(null,"Iniciando con: "+(Entrada_Productos.getText())+" productos");
            // ponemos el valor a la variable global productos
            Proyecto.Productos = Integer.parseInt(Entrada_Productos.getText());
            // variable para las matrices
            int productos = Integer.parseInt(Entrada_Productos.getText());
            String[][] elementos = new String[productos+1][21];
            String[][] tiempo = new String[productos+1][5];
            // Maquetaci√≥n de las matrices
            // Matriz Elementos
            elementos[0][0] = "Producto";elementos[0][1] = "Primera Linea";elementos[0][2] = "1";elementos[0][3] = "2";
            elementos[0][4] = "3";elementos[0][5] = "Segunda Linea";elementos[0][6] = "1";elementos[0][7] = "2";
            elementos[0][8] = "3";elementos[0][9] = "4";elementos[0][10] = "5";elementos[0][11] = "6";
            elementos[0][12] = "Tercera Linea";elementos[0][13] = "1";elementos[0][14] = "2";elementos[0][15] = "3";
            elementos[0][16] = "4";elementos[0][17] = "5";elementos[0][18] = "6";elementos[0][19] = "7";
            elementos[0][20] = "8";
            // Matriz Tiempo
            tiempo[0][0] = "Producto";tiempo[0][1] = "Primera Linea";tiempo[0][2] = "Segunda Linea";tiempo[0][3] = "Tercera linea";
            tiempo[0][4] = "Total";
            // guardamos las matrices en nuestras variables locales
            Proyecto.Matriz_Elementos = elementos;
            Proyecto.Matriz_Tiempos = tiempo;
            Proyecto.Iniciar();
        }
        catch (Exception error){
            JOptionPane.showMessageDialog(null, "se presento un error al iniciar el programa " +
                    "\ningrese un numero por favor\n"+error);
        }
    }//GEN-LAST:event_Boton_iniciarActionPerformed

